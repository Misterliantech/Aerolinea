    private void aplicarFiltroNumeros() {

        javax.swing.text.AbstractDocument document = (javax.swing.text.AbstractDocument) cedulatext.getDocument();
        
        document.setDocumentFilter(new DocumentFilter() {
            @Override
            public void insertString(FilterBypass fb, int offset, String string, AttributeSet attr) throws BadLocationException {
                if (string.matches("[0-9]*")) { 
                    super.insertString(fb, offset, string, attr);
                }
            }

            @Override
            public void replace(FilterBypass fb, int offset, int length, String string, AttributeSet attrs) throws BadLocationException {
                if (string.matches("[0-9]*")) { 
                    super.replace(fb, offset, length, string, attrs);
                }
            }
        });
    }

   private void cargarNacionalidades() {
        String query = "SELECT nombre_pais FROM nacionalidades";  // Consulta SQL

        List<String> nacionalidades = new ArrayList<>();  // Lista para almacenar los países
    
        try (Connection conn = ConexionBD.getConexion(); 
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(query)) {

            // Llenar la lista de nacionalidades con los países obtenidos de la consulta
            while (rs.next()) {
                nacionalidades.add(rs.getString("nombre_pais"));
            }

            // Cargar los países al JComboBox
            for (String pais : nacionalidades) {
                comboBoxNacionalidades.addItem(pais);
            }

        } catch (SQLException e) {
            // Si ocurre un error, mostrar un mensaje
            JOptionPane.showMessageDialog(this, "Error al cargar las nacionalidades: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();  // Imprime el detalle del error en la consola
        }
        
   }
