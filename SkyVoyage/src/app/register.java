     private boolean validarCampos() {
        // Obtener los valores de los campos
        String nombre = nombretext.getText();
        String correo = emailtext.getText();
        String contrasena = contraseña1.getText();
        String repetirContrasena = contraseña2.getText();

        // Comprobar si algún campo está vacío
        if (nombre.isEmpty() || correo.isEmpty() || contrasena.isEmpty() || repetirContrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar completos.", "Error", JOptionPane.ERROR_MESSAGE);
            return false; // Devuelve falso si algún campo está vacío
        }

        // Comprobar si las contraseñas coinciden
        if (!contrasena.equals(repetirContrasena)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden. Por favor, verifica.", "Error", JOptionPane.ERROR_MESSAGE);
            return false; // Devuelve falso si las contraseñas no coinciden
        }

        // Si todas las comprobaciones son correctas, devolver verdadero
        return true;
    }
    
     
    private void guardarUsuario() {
        String nombreUsuario = nombreusuariotext.getText(); // Nombre de usuario
        String nombre = nombretext.getText();               // Nombre completo
        String correo = emailtext.getText();               // Correo electrónico
        String contrasena = new String(contraseña1.getPassword()); // Contraseña
        String cedula = cedulatext.getText();               // Cédula
        String nacionalidad = (String) comboBoxNacionalidades.getSelectedItem(); // Nacionalidad seleccionada

        // Verificar que todos los campos estén llenos
        if (nombreUsuario.isEmpty() || nombre.isEmpty() || correo.isEmpty() || contrasena.isEmpty() || cedula.isEmpty() || nacionalidad.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validar si el nombre de usuario, correo o cédula ya existe
        if (existeUsuario(nombreUsuario, correo, cedula)) {
            return; // Si alguno existe, no continuar con la inserción
        }

        // Realiza la conexión y guardado en la base de datos
        try (Connection conn = ConexionBD.getConexion()) {
            String query = "INSERT INTO usuarios (nombre_usuario, nombre, correo, contrasena, cedula, nacionalidad) VALUES (?, ?, ?, ?, ?, ?)";
            try (PreparedStatement stmt = conn.prepareStatement(query)) {
                stmt.setString(1, nombreUsuario);      // Set nombre de usuario
                stmt.setString(2, nombre);             // Set nombre completo
                stmt.setString(3, correo);             // Set correo
                stmt.setString(4, contrasena);         // Set contraseña
                stmt.setString(5, cedula);             // Set cédula
                stmt.setString(6, nacionalidad);       // Set nacionalidad

                int rowsInserted = stmt.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(this, "Usuario guardado con éxito", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private boolean existeUsuario(String nombreUsuario, String correo, String cedula) {
    try (Connection conn = ConexionBD.getConexion()) {
        // Consulta para verificar si el nombre de usuario ya existe
        String query = "SELECT COUNT(*) FROM usuarios WHERE nombre_usuario = ? OR correo = ? OR cedula = ?";
        try (PreparedStatement stmt = conn.prepareStatement(query)) {
            stmt.setString(1, nombreUsuario);
            stmt.setString(2, correo);
            stmt.setString(3, cedula);

            ResultSet rs = stmt.executeQuery();
            if (rs.next() && rs.getInt(1) > 0) {
                JOptionPane.showMessageDialog(this, "El nombre de usuario, correo o cédula ya está registrado.", "Error", JOptionPane.ERROR_MESSAGE);
                return true;  // Si algún campo ya existe, retornar true
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al verificar los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    return false; // Si no se encontró nada, retornar false
}
